package Array_practice;

public class Arrayclass_1 {
    public static void main(String[] args) {
        String name[]=new String[3];
        name[0]="vaibhav";
        name[1]="roshni";
        name[2]="vicky";
        for (int i=0; i<3; i++){
            System.out.println(name[i]);
        }

        String emoplo[]={"vaibha\nrahul\nroshni"};
        for (int i=0; i<3; i++){
            System.out.println(emoplo[i]);
        }

    }
}
