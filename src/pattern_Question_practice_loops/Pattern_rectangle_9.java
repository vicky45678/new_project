package pattern_Question_practice_loops;

public class Pattern_rectangle_9 {
    public static void main(String[] args) {

        int a=7, b=10;

        for (int i=1; i<=a; i++){
            for (int j=1; j<=b; j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
